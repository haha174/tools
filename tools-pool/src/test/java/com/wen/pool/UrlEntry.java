package com.wen.pool;

public class UrlEntry {
    private String id;
    private String url;
    private String oriFrom;
    private String _delete_;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getOriFrom() {
        return oriFrom;
    }

    public void setOriFrom(String oriFrom) {
        this.oriFrom = oriFrom;
    }

    public String get_delete_() {
        return _delete_;
    }

    public void set_delete_(String _delete_) {
        this._delete_ = _delete_;
    }
}
